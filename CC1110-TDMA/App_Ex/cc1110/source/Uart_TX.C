#include <Uart.h>


/*******************************************************************************
描述：
    串口发送字符串函数
函数名：void UartTX_Send_String(char *data,int len)
data:数据
len :数据长度
*******************************************************************************/
void UartTX_Send_String(char *Data,int len)
{
  int j;
  for(j=0;j<len;j++)
  {
    U0DBUF = *Data++;
    while(UTX0IF == FALSE);
    UTX0IF = FALSE;
  }
}
